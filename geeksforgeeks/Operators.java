package geeksforgeeks;

public class Operators {

	public static void main(String[] args) {
	// Airthmatic operator 
		int x = 10 ;  System.out.println("x++ = " + x++ + "\nx = "+x );
        int y = 5;  System.out.println("++y = " + ++y + "\ny = "+y ); 
        
        // Logical operators 
        
        System.out.println(true&&false); System.out.println(false^false);
        // Relational operator
        // Bitwise Operators 
        System.out.println( 2&3 );  System.out.println(2|3); System.out.println(-2&3); System.out.println(~ 2); 
	}

}
