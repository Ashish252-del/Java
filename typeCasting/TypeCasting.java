package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
  byte x = 4;
  int y = x;
  System.out.println(y);
	
  
   int p = 10;
   double q = p;
   System.out.println(q);
		
		
		double myDouble = 2.8496;
		int myInt = (int)myDouble;
		System.out.println(myDouble);
		System.out.println(myInt);
		
	}

}
 